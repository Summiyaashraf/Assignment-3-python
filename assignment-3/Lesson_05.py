# If-elif-else Example
num = int(input("Enter a number: "))
if num > 0:
    print("Positive number")
elif num < 0:
    print("Negative number")
else:
    print("Zero")

# For Loop Example
for i in range(1, 6):
    print(f"Iteration {i}")

# While Loop Example
count = 0
while count < 3:
    print(f"Count: {count}")
    count += 1
