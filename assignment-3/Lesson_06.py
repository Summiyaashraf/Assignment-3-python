# List Example
fruits = ["Apple", "Banana", "Mango"]
fruits.append("Orange")  # Adding an element
print(f"Fruits List: {fruits}")

# Tuple Example
days = ("Monday", "Tuesday", "Wednesday")
print(f"First day: {days[0]}")  # Accessing elements

# Dictionary Example
student = {
    "name": "Summiya",
    "age": 21,
    "course": "Python"
}
print(f"Student Name: {student['name']}")
